/**
 *
 * @param {value} number
 *
 */
export function secondToTime(value) {
  let theTime = parseInt(value) //
  let middle = 0 //
  let hour = 0 //

  if (theTime > 60) {
    middle = parseInt(theTime / 60)
    theTime = parseInt(theTime % 60)
    if (middle > 60) {
      hour = parseInt(middle / 60)
      middle = parseInt(middle % 60)
    }
  }

  let result = parseInt(theTime) == 0 ? '' : `${parseInt(theTime)}s`
  if (middle > 0) {
    result = '' + parseInt(middle) + 'min' + result
  }
  if (hour > 0) {
    result = '' + parseInt(hour) + 'hr' + result
  }
  return result
}
